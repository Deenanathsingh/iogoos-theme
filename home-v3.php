<script>
	const slider = $(".slider-item");
	slider.slick({
		autoplay: true,
		autoplaySpeed: 1500,
		dots: true,
		mousewheelControl: 1
	});
	slider.on("wheel", function(e) {
		e.preventDefault();

		if (e.originalEvent.deltaY < 0) {
			$(this).slick("slickNext");
		} else {
			$(this).slick("slickPrev");
		}
	});
</script>
